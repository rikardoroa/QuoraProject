CREATE DATABASE PRUEBA
GO

CREATE TABLE FECHAS
(
ID INT IDENTITY (1,1) NOT NULL,
FECHA_UNO DATETIME NOT NULL,
FECHA_DOS DATE NOT NULL,
HORA VARCHAR(50) NOT NULL,
CONSTRAINT PK_ID PRIMARY KEY(ID)

)

USE PRUEBA
GO

INSERT  INTO FECHAS (FECHA_UNO,FECHA_DOS,HORA) VALUES (GETDATE(),'01/10/2019','18:00')

SELECT *,
CONCAT(FECHA_DOS,' ',HORA)AS FECHA_CREADA,
 DATEDIFF(HOUR,FECHA_UNO, CONVERT (VARCHAR(80),(CONCAT(FECHA_DOS,' ',HORA)),101))AS HORAS_RESPUESTA,
  CONCAT( DATEDIFF(MINUTE,FECHA_UNO, CONVERT (VARCHAR(80),(CONCAT(FECHA_DOS,' ',HORA)),101)),' ','MINUTOS') AS TIEMPO_DE_RESPUESTA
    FROM FECHAS