
DECLARE @CNSREQ VARCHAR(30)

SET @CNSREQ=(SELECT  
 REQUISICIONES.CNSREQ AS CONSECUTIVO FROM REQUISICIONES   
 INNER JOIN ITEMSREQ ON ITEMSREQ.CEDULA = REQUISICIONES.CEDULA AND ITEMSREQ.CARGO = REQUISICIONES.CARGO   
 WHERE REQUISICIONES.REVISION IS NULL AND REQUISICIONES.APROBACION IS NULL AND REQUISICIONES.FIRMA_REVISION IS NULL 
 AND  REQUISICIONES.CNSREQ NOT IN (SELECT CONSECUTIVO  FROM RQITEMSTEMPORAL)	GROUP BY
										REQUISICIONES.CARGO,    
	   				  	   				REQUISICIONES.CEDULA,    
	   				  	   				ITEMSREQ.CANTIDAD,    
	   				  	   				ITEMSREQ.ITEM ,   
	   				  	   				REQUISICIONES.CNSREQ,   
	   				  	   				REQUISICIONES.SOLICITANTE,   
	   				  	   				REQUISICIONES.FECHA_SOLICITUD,   
	   				  	   				ITEMSREQ.FECHA_SOLICITUD,   
	   				  	   				REQUISICIONES.AREA  
										 
	   		HAVING CONVERT(VARCHAR(16),ITEMSREQ.FECHA_SOLICITUD,20) = CONVERT(VARCHAR(16),REQUISICIONES.FECHA_SOLICITUD,20))   

	
	PRINT @CNSREQ