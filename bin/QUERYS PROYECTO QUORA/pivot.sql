

SELECT  *,ISNULL([Disco duro 1tb],0) FROM (SELECT REQUISICIONES.CNSREQ AS REQ, REQUISICIONES.SOLICITANTE AS SOLICITANTE, REQUISICIONES.CARGO AS CARGO, 
       REQUISICIONES.FECHA_SOLICITUD, ITEMSREQ.ITEM AS ITEM, ITEMSREQ.CANTIDAD AS CANTIDAD FROM REQUISICIONES
	   INNER JOIN ITEMSREQ ON
	   ITEMSREQ.CNSREQ = REQUISICIONES.CNSREQ
	   WHERE REQUISICIONES.APROBACION=1)AS Y
	 PIVOT
	   (
	   SUM (CANTIDAD)  
       FOR ITEM IN  
       (  [Disco duro 1tb],[disco duro 10 tb],[Cable utp x  1 metro],[Cable hdmi x 50 metros],[memoria ram de 4gb] )  
      )  AS pvt
	   
	  
	 